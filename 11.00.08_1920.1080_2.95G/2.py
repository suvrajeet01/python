#functions

#creating a function
import turtle

sjb_turtle = turtle.Turtle()
def square():
	sjb_turtle.forward(100)
	sjb_turtle.right(90)
	sjb_turtle.forward(100)
	sjb_turtle.right(90)
	sjb_turtle.forward(100)
	sjb_turtle.right(90)
	sjb_turtle.forward(100)
#calling the function
square()
sjb_turtle.forward(100)
square()
