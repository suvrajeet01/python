#functions on numbers
num1 = -5.87
print(abs(num1))        #ab() function provides the absolute value of a number
print(pow(4 , 4))       #pow() function returns the value of a number raised to the power of another number ;4^4
print(pow(4 , 6))
print(max(4, 6))        #max() function returns the larger number from the inputs given
print(max(4, 6, 9))
print(min(2,5,9,-1,0))  #min() function returns the lowest value amongst the inputs given
print(round(3.2))       #round() functions rounds off to the nearest value of the given number
print(round(3.7))

